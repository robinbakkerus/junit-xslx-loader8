package flca.xlsx.util;

public class XlsxConfigValues {
	private int maxRows;
	private int maxColumns;
	private String fqnSpecialConvertUtils;
	
	public int getMaxRows() {
		return maxRows;
	}
	public void setMaxRows(int maxRows) {
		this.maxRows = maxRows;
	}
	public int getMaxColumns() {
		return maxColumns;
	}
	public void setMaxColumns(int maxColumns) {
		this.maxColumns = maxColumns;
	}
	public String getFqnSpecialConvertUtils() {
		return fqnSpecialConvertUtils;
	}
	public void setFqnSpecialConvertUtils(String fqnSpecialConvertUtils) {
		this.fqnSpecialConvertUtils = fqnSpecialConvertUtils;
	}

}
